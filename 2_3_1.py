x = 10
y = 3
 
sum_ = x + y
print("#",type(sum_), sum_)
# <class 'int'> 13

difference = x - y
print("#", type(difference), difference)
# <class 'int'> 7

product = x * y
print("#",type(product), product)
# <class 'int'> 30
      
quotient = x / y
print("#", type(quotient),quotient)
# <class 'float'> 3.3333333333333335

exponential = x ** y
print("#", type(exponential),exponential)
# <class 'int'> 1000
      
euclidean_quotient = x // y
print("#", type(euclidean_quotient), euclidean_quotient)
# <class 'int'> 3
      
remainder = x % y
print("#", type(remainder), remainder)
# <class 'int'> 1
